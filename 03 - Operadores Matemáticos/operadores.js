//concatenação de palavras (strings)
var eu = "Mamis"
var gosto = "ama"
var dele = "o"
var gato = "Edinho"
console.log(eu + " " + gosto + " " + dele + " " + gato)

//soma e subtração de números
var primeiro_numero = 10
